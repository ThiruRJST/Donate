    private void getInfo(final View view){
        RequestQueue requestQueue = Volley.newRequestQueue(getActivity());
        JsonArrayRequest jsonArrayRequest = new JsonArrayRequest(Request.Method.GET, url, null, new Response.Listener<JSONArray>() {
            @Override
            public void onResponse(JSONArray response) {

                try{
                    for(int i = 0;i<response.length();i++){
                        JSONObject post = response.getJSONObject(i);
                        String nm = post.getString("Name");
                        String dp = post.getString("Description");


                        Name.add(nm);
                        Description.add(dp);

                        ListView lv = view.findViewById(R.id.list1);
                        ArrayAdapter<String> lvad = new ArrayAdapter<>(getActivity(),android.R.layout.simple_list_item_1,Description);
                        lv.setAdapter(lvad);


                    }


                } catch (JSONException e) {
                    e.printStackTrace();
                }

            }
        }, new Response.ErrorListener() {
            @Override
            public void onErrorResponse(VolleyError error) {
                Toast.makeText(getActivity(),error.toString(), Toast.LENGTH_SHORT).show();
            }
        });
        requestQueue.add(jsonArrayRequest);
    }
